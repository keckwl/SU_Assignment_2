def lid_predict(wav, chunk_frames=512):
    feats = get_feats(wav)
    T = feats.shape[0]
    preds = np.zeros(T, dtype=np.int64)
    with torch.no_grad():
        for s in range(0, T, chunk_frames):
            chunk = feats[s:s+chunk_frames].unsqueeze(0)
            logits = lid(chunk).squeeze(0)
            preds[s:s+chunk_frames] = torch.argmax(logits, dim=-1).cpu().numpy()
    switches = [round(i*HOP_MS/1000,3) for i in range(1,T) if preds[i]!=preds[i-1]]
    return preds, switches

