def extract_lfcc(wav, sr=16000, n_lfcc=20, n_fft=512, hop=160):
    x = wav.cpu().numpy().astype(np.float32)
    win = np.hanning(n_fft)
    frames = np.array([x[i:i+n_fft]*win for i in range(0, len(x)-n_fft, hop)], dtype=np.float32)
    if len(frames) == 0:
        return torch.zeros(1, n_lfcc)
    spec = np.abs(np.fft.rfft(frames, axis=1))**2
    nf, nb = 40, spec.shape[1]
    fb = np.zeros((nf, nb), np.float32)
    freqs = np.linspace(0, sr/2, nb)
    ctrs = np.linspace(0, sr/2, nf+2)
    for m in range(nf):
        l, c, r = ctrs[m], ctrs[m+1], ctrs[m+2]
        for k in range(nb):
            f = freqs[k]
            if l <= f <= c:
                fb[m,k] = (f-l)/(c-l+1e-8)
            elif c < f <= r:
                fb[m,k] = (r-f)/(r-c+1e-8)
    log_fb = np.log(spec@fb.T + 1e-10)
    return torch.from_numpy(dct(log_fb, type=2, n=n_lfcc, axis=1, norm='ortho').astype(np.float32))

