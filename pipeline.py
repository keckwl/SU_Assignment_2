def transcribe_chunked(wav, sr=SR, chunk_s=30):
    segs = []
    for start in range(0, len(wav), chunk_s*sr):
        chunk = wav[start:start+chunk_s*sr]
        if chunk.shape[0] < sr:
            continue
        a = chunk.numpy().astype(np.float32)
        a /= np.abs(a).max() + 1e-8
        r = wmodel.transcribe(a, beam_size=5, temperature=0.0,
                word_timestamps=True, task='transcribe',
                condition_on_previous_text=True,
                no_speech_threshold=0.6, logprob_threshold=-1.0)
        lang = r.get('language', 'hi')
        for seg in r['segments']:
            segs.append({
                'start': start/sr + seg['start'],
                'end':   start/sr + seg['end'],
                'lang':  lang,
                'text':  boost_terms(seg['text'].strip())
            })
        del a, r
        if DEVICE == 'cuda':
            torch.cuda.empty_cache()
    return segs

