def fgsm(wav, eps, target):
    delta = torch.zeros_like(wav.to(DEVICE), requires_grad=True)
    wav_pert = (wav.to(DEVICE) + delta).unsqueeze(0)
    feats = mfcc_fn(wav_pert).squeeze(0).T
    lid.train()
    logits = lid(feats.unsqueeze(0)).squeeze(0)
    T = logits.shape[0]
    F.cross_entropy(logits, torch.full((T,), target, dtype=torch.long, device=DEVICE)).backward()
    lid.eval()
    if delta.grad is None:
        return wav.clone()
    return (wav.to(DEVICE) + eps * delta.grad.sign()).clamp(-1, 1).detach().cpu()

