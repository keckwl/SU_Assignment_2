def _tok(t):
    return re.findall(r'[a-z0-9]+', t.lower())

class NgramLM:
    def __init__(self, n=3):
        self.n = n
        toks = _tok(CORPUS)
        self.tech = TECH | {t for t in toks if len(t) > 6}
        self.counts = defaultdict(lambda: defaultdict(int))
        for i in range(len(toks)-n+1):
            self.counts[tuple(toks[i:i+n-1])][toks[i+n-1]] += 1
    def is_tech(self, w):
        return w.lower() in self.tech

ngram = NgramLM()

def _ed(a, b):
    if abs(len(a)-len(b)) > 2:
        return 99
    dp = list(range(len(b)+1))
    for ca in a:
        prev, dp[0] = dp[:], dp[0]+1
        for j, cb in enumerate(b, 1):
            dp[j] = prev[j-1] if ca==cb else 1+min(prev[j],dp[j-1],prev[j-1])
    return dp[len(b)]

def boost_terms(text):
    out = []
    for w in text.split():
        cw = w.lower().strip('.,!?;:')
        if len(cw) < 5 or ngram.is_tech(cw):
            out.append(w)
            continue
        best, bd = None, 2
        for t in ngram.tech:
            if abs(len(t)-len(cw)) > 2:
                continue
            d = _ed(cw, t)
            if d < bd:
                bd, best = d, t
        out.append(best if best else w)
    return ' '.join(out)

