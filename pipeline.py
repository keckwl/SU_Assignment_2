def load_mono(path, sr=16000):
    w, s = torchaudio.load(path)
    if w.shape[0] > 1:
        w = w.mean(0, keepdim=True)
    if s != sr:
        w = torchaudio.functional.resample(w, s, sr)
    return w.squeeze(0)

def spectral_subtract(wav, n_fft=512, alpha=2.0, beta=0.002, noise_frames=20):
    x = wav.numpy().astype(np.float64)
    hop = n_fft // 2
    win = np.hanning(n_fft)
    idx = range(0, len(x) - n_fft, hop)
    frames = np.array([x[i:i+n_fft]*win for i in idx], dtype=np.float64)
    S = np.fft.rfft(frames, axis=1)
    mag = np.abs(S)
    phase = np.angle(S)
    noise = mag[:noise_frames].mean(0)
    mag2 = np.maximum(mag - alpha*noise, beta*mag)
    fc = np.fft.irfft(mag2 * np.exp(1j*phase), axis=1)
    out = np.zeros(len(x), np.float64)
    cnt = np.zeros(len(x), np.float64)
    for k, i in enumerate(idx):
        out[i:i+n_fft] += fc[k]*win
        cnt[i:i+n_fft] += win**2
    out /= np.where(cnt < 1e-8, 1e-8, cnt)
    pk = np.abs(out).max()
    if pk > 0:
        out = out/pk*0.95
    return torch.from_numpy(out.astype(np.float32))

