class _Blk(nn.Module):
    def __init__(self, d, h):
        super().__init__()
        self.attn = nn.MultiheadAttention(d, h, dropout=0.1, batch_first=True)
        self.ff = nn.Sequential(nn.Linear(d,512), nn.GELU(), nn.Dropout(0.1), nn.Linear(512,d))
        self.n1 = nn.LayerNorm(d)
        self.n2 = nn.LayerNorm(d)
    def forward(self, x):
        a, _ = self.attn(x, x, x)
        x = self.n1(x+a)
        return self.n2(x+self.ff(x))

class LID(nn.Module):
    def __init__(self, in_dim=40, d=128, h=4):
        super().__init__()
        self.pre = nn.Sequential(nn.Linear(in_dim,d), nn.LayerNorm(d), nn.GELU())
        self.blocks = nn.ModuleList([_Blk(d,h) for _ in range(2)])
        self.cls = nn.Linear(d,2)
    def forward(self, x):
        x = self.pre(x)
        for b in self.blocks:
            x = b(x)
        return self.cls(x)

def get_feats(wav):
    with torch.no_grad():
        return mfcc_fn(wav.to(DEVICE).unsqueeze(0)).squeeze(0).T

def pseudo_labels(wav):
    x = wav.cpu().numpy().astype(np.float32)
    T_max = (len(x) - FRAME) // HOP + 1
    lbl = np.zeros(T_max, dtype=np.int64)
    for i in range(T_max):
        f = x[i*HOP: i*HOP+FRAME]
        if len(f) < FRAME:
            continue
        f = f - f.mean()
        if np.abs(f).max() < 1e-6:
            continue
        zcr = float(np.mean(np.abs(np.diff(np.sign(f))))/2)
        lbl[i] = 1 if zcr > 0.12 else 0
    return torch.from_numpy(lbl)

