def extract_prosody(wav, sr=SR, frame_ms=25, hop_ms=10):
    x = wav.cpu().numpy().astype(np.float32)
    frame_n = int(sr * frame_ms / 1000)
    hop_n = int(sr * hop_ms / 1000)
    n = (len(x) - frame_n) // hop_n + 1
    f0 = np.zeros(n, np.float32)
    eng = np.zeros(n, np.float32)
    lo, hi = int(sr/400), int(sr/60)
    for i in range(n):
        f = x[i*hop_n: i*hop_n+frame_n]
        if len(f) < frame_n:
            break
        f = f - f.mean()
        eng[i] = float(np.sqrt((f**2).mean() + 1e-10))
        if np.abs(f).max() < 1e-6:
            continue
        n_fft = 1 << (2*frame_n - 1).bit_length()
        F = np.fft.rfft(f, n=n_fft)
        acf = np.fft.irfft(F * np.conj(F))[:frame_n]
        if acf[0] < 1e-10:
            continue
        pk = np.argmax(acf[lo:hi]) + lo
        if acf[pk]/acf[0] > 0.3:
            f0[i] = float(sr/pk)
    voiced = f0 > 0
    if voiced.sum() > 2:
        mu, sd = f0[voiced].mean(), f0[voiced].std()+1e-6
        f0[voiced] = (f0[voiced]-mu)/sd
    return f0, eng

