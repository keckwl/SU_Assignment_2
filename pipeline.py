def wer(hyp, ref):
    h, r = hyp.lower().split(), ref.lower().split()
    if not r or not h:
        return 0.0
    dp = list(range(len(h)+1))
    for ri in r:
        prev, dp[0] = dp[:], dp[0]+1
        for j, hi in enumerate(h, 1):
            dp[j] = prev[j-1] if ri==hi else 1+min(prev[j],dp[j-1],prev[j-1])
    return min(dp[len(h)]/len(r), 1.0)

def mcd_score(s_wav, r_wav, sr=22050):
    mfcc_t = torchaudio.transforms.MFCC(
        sample_rate=sr, n_mfcc=13,
        melkwargs={'n_fft':1024,'hop_length':256,'n_mels':80})
    n = min(s_wav.shape[0], r_wav.shape[0], sr*30)
    sm = mfcc_t(s_wav[:n].unsqueeze(0)).squeeze(0).numpy()
    rm = mfcc_t(r_wav[:n].unsqueeze(0)).squeeze(0).numpy()
    nc = min(sm.shape[1], rm.shape[1])
    sm = (sm-sm.mean(1,keepdims=True))/(sm.std(1,keepdims=True)+1e-8)
    rm = (rm-rm.mean(1,keepdims=True))/(rm.std(1,keepdims=True)+1e-8)
    cost = cdist(sm[1:,:nc].T, rm[1:,:nc].T, metric='euclidean')
    N, M = cost.shape
    dp = np.full((N,M), np.inf)
    dp[0,0] = cost[0,0]
    for i in range(1,N):
        dp[i,0] = dp[i-1,0]+cost[i,0]
    for j in range(1,M):
        dp[0,j] = dp[0,j-1]+cost[0,j]
    for i in range(1,N):
        for j in range(1,M):
            dp[i,j] = cost[i,j]+min(dp[i-1,j],dp[i,j-1],dp[i-1,j-1])
    i, j = N-1, M-1
    path_cost = []
    while i > 0 or j > 0:
        path_cost.append(cost[i,j])
        if i == 0:
            j -= 1
        elif j == 0:
            i -= 1
        else:
            s = np.argmin([dp[i-1,j],dp[i,j-1],dp[i-1,j-1]])
            if s == 0:
                i -= 1
            elif s == 1:
                j -= 1
            else:
                i -= 1
                j -= 1
    return float((10/np.log(10))*np.sqrt(2*np.mean(np.array(path_cost)**2)))

