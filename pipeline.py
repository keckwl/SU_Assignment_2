def dtw_warp(synth, ref_f0, ref_eng, synth_sr=22050, hop_ms=10):
    hop_n = int(synth_sr * hop_ms / 1000)
    sf0, seng = extract_prosody(synth, sr=synth_sr)
    rl, sl = min(len(ref_f0),400), min(len(sf0),400)
    rx, sx = ref_f0[:rl], sf0[:sl]
    N, M = rl, sl
    C = np.full((N,M), np.inf, np.float32)
    C[0,0] = abs(rx[0]-sx[0])
    for i in range(1,N):
        C[i,0] = C[i-1,0]+abs(rx[i]-sx[0])
    for j in range(1,M):
        C[0,j] = C[0,j-1]+abs(rx[0]-sx[j])
    for i in range(1,N):
        for j in range(1,M):
            C[i,j] = abs(rx[i]-sx[j])+min(C[i-1,j],C[i,j-1],C[i-1,j-1])
    x = synth.cpu().numpy().astype(np.float32)
    scale = np.ones(len(x), np.float32)
    n_fr = min(len(seng), len(ref_eng), sl)
    for i in range(n_fr):
        sc = np.clip(ref_eng[i]/(seng[i]+1e-8), 0.5, 2.0)
        scale[i*hop_n:(i+1)*hop_n] = sc
    out = x * scale[:len(x)]
    pk = np.abs(out).max()
    if pk > 0:
        out = out/pk*0.95
    return torch.from_numpy(out)

